    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
          String jenis = null;
       if(rbLaki.isSelected()){
           jenis = "Laki - Laki";
       }else if (rbPerempuan.isSelected()){
        jenis = "Perempuan";
    }
       try{
           String sql =" update pustakawan set no_ktp=?,nama=?,jenis_kelamin=?,Agama=?,no_hp=?,alamat=? WHERE id_pustakawan ='"+txtId.getText()+"'";
           
          PreparedStatement stat = conn.prepareStatement(sql);
          stat.setString(1, txtKtp.getText());
          stat.setString(2, txtNm.getText());
          stat.setString(3, jenis);
          stat.setString(4, cbAgama.getSelectedItem().toString());
          stat.setString(5, txtHp.getText());
          stat.setString(6, txtAlamat.getText());
          
          stat.executeUpdate();
          JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
          kosong();
          txtId.requestFocus();
          cbAgama.setSelectedItem(1);
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null, "Data Gagal Diubah" +e);
       }
       dataTabel();
    }//GEN-LAST:event_btnUbahActionPerformed

