    private void dataTabel(){
           Object[] Baris ={"ID Pustakawan","No KTP","Nama","Jenis Kelamin","Agama","No. HP","Alamat"};
            tabmode = new DefaultTableModel(null,Baris);
            String cariItem = txtCari.getText();
            
            try{
                String sql = "SELECT * FROM pustakawan where id_pustakawan like '%"+cariItem+"%' or nama like '%"+cariItem+"%' order by id_pustakawan asc";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()){
                    tabmode.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                        hasil.getString(7)
                    });
                    
                }
                tblPstkw.setModel(tabmode);
            }catch(SQLException e){
             JOptionPane.showMessageDialog(null, "Data Gagal dipanggil"+e);
            }
    }
    
     protected void autoNumber(){
        try{
            String sql = "SELECT id_pustakawan FROM pustakawan order by id_pustakawan asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            txtId.setText("PW00001");
            while(rs.next()){
                String kd_pinjam = rs.getString("id_pustakawan").substring(2);
                int AN = Integer.parseInt(kd_pinjam)+1;
                String nol = "";
                
                if(AN<010)
                {nol = "000";}
                else if (AN < 100)
                {nol = "00";}
                else if (AN < 1000)
                {nol = "0";}
                else if (AN < 10000)
                {nol = "";}
                txtId.setText("PW" + nol + AN);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Auto Number Gagal"+e);
        }
    }

