    private void dataTabel(){
        String cariItem = txtCari.getText();
            
            try{
                String sql = "SELECT * FROM pengembalian where kode_pengembalian like '%"+cariItem+"%' or nama_siswa like '%"+cariItem+"%' order by kode_pengembalian asc";
                Statement stat = conn.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()){
                    tabmode.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                        hasil.getString(7),
                        hasil.getString(8),
                        hasil.getString(9)
                    });
                    
                }
                tblKem.setModel(tabmode);
            }catch(SQLException e){
             JOptionPane.showMessageDialog(null, "Data Gagal dipanggil"+e);
            } 
    }
    
