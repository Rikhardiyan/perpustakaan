    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
       SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fd = sdf.format(tglPin.getValue());
         String fd2 = sdf.format(tglKem.getValue());
        
        if(txtNis.getText().trim().equals("")){
           JOptionPane.showMessageDialog(null, "Nis Tidak Boleh Kosong");
       } else if(txtSis.getText().trim().equals("")){
           JOptionPane.showMessageDialog(null, "Nama Tidak Boleh Kosong");
       } else if (txtNmPus.getText().trim().equals("")){
           JOptionPane.showMessageDialog(null, "Nama Tidak Boleh Kosong");
       }else if(txtJdl.getText().trim().equals("")){
           JOptionPane.showMessageDialog(null, "Judul Tidak Boleh Kosong");
       }else{
           
       }
       try{
           
           
           String sql = "insert into pengembalian values"+
                   "('"+txtKode.getText()+
                   "','"+txtNis.getText()+
                   "','"+txtSis.getText()+
                   "','"+txtNmPus.getText()+
                   "','"+txtJdl.getText()+
                   "','"+fd+
                   "','"+fd2+
                   "',datediff(tanggal_kembali,tanggal_pinjam)-7,('"+txtDenda.getText()+"')*jumlah_telat)";
           PreparedStatement stat = conn.prepareStatement(sql);
           stat.executeUpdate();
           JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
           kosong();
           
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
       }
       aktif();
       dataTabel();
      
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblKemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKemMouseClicked
        int index=tblKem.getSelectedRow();
       txtKode.setText((String)tabmode.getValueAt(index, 0));
       txtNis.setText((String)tabmode.getValueAt(index, 1));
    txtSis.setText((String)tabmode.getValueAt(index, 2));
    txtNmPus.setText((String)tabmode.getValueAt(index, 3));
    txtJdl.setText((String)tabmode.getValueAt(index, 4));
    try{
        int srow = tblKem.getSelectedRow();
        Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabmode.getValueAt(srow, 5));
        tglPin.setValue(date);
        Date date2 = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabmode.getValueAt(srow,6));
       
        tglKem.setValue(date2);
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
    txtDenda.setText((String)tabmode.getValueAt(index, 8));
    }//GEN-LAST:event_tblKemMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
         int ok = JOptionPane.showConfirmDialog(null, "Hapus","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if (ok==0){
                String sql = "DELETE FROM pengembalian WHERE kode_pengembalian='"+txtKode.getText()+"'";
                try{
                    PreparedStatement stat = conn.prepareStatement(sql);
                    stat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                    kosong();
                    txtNis.requestFocus();
                }catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Data Gagal Dihapus"+e);
                }
                aktif();
                dataTabel();
              //  autoNumber();
            }
    }//GEN-LAST:event_btnHapusActionPerformed

